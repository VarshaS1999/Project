package pkg;

public class Datatypes {

	public static void main(String[] args) {
		// Datatypes
		byte b=24;
		System.out.println("value of byte="+b);
		int a=1235;
		System.out.println("value of int="+a);
		short s=234;
		System.out.println("value of short="+s);
		long l=2345l;
		System.out.println("value of long="+l);
		float f=2.3f;
		System.out.println("value of float="+f);
		char c='v';
		System.out.println("value of char="+c);
		boolean g=20>10;
		System.out.println(g);
		double d=23.45;
		System.out.println("value of d="+d);
	String v="Varsha";
	System.out.println("value of String="+v);
	
	
		
			

	}

}
